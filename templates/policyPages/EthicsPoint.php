<div class="project-list"><h2>FREQUENTLY ASKED QUESTIONS</h2></div>
<div class="project-list-content">
    <p><strong>How will I use the EthicsPoint Hotline/Website?</strong></p>
    <p>You may file a report online at <a href="https://carnegiescience.ethicspoint.com">https://carnegiescience.ethicspoint.com</a> &nbsp;or call the <strong><u>24/7 EthicsPoint Hotline at (844) 680-0876 </u></strong>to report activities that may involve misconduct or violations of Carnegie’s institutional policies.</p>
    <p>&nbsp;</p>
    <p><strong>What happens when I file a report using EthicsPoint?</strong></p>
    <ul>
        <li><strong>By phone:</strong></li>
    </ul>
    <ol>
        <li>Follow the telephone prompts</li>
        <li>A communication specialist will ask a series of questions</li>
        <li>The communication specialist will issue caller a numeric report key and password for follow up</li>
    </ol>
    <ul>
        <li><strong>By internet: </strong></li>
    </ul>
    <ol>
        <li>Follow the website prompts</li>
        <li>Respond to the on-line questions</li>
        <li>You will receive a numeric report key and password for follow up</li>
    </ol>
    <p><strong>Will I get updates after submitting a report?</strong></p>
    <p>Within 5-7 business days, you may use your report key and password to check your report as there may be follow up questions, additional information for you to review, or additional information that you will need to submit about the incident.</p>
    <p>&nbsp;</p>
    <p><strong>Will my report be confidential?</strong></p>
    <p>The institution (and Navex) is committed to safeguarding the confidentiality of individuals who submit reports and the content reported. The information you provide will be sent to Carnegie by EthicsPoint on a confidential and anonymous basis if you should choose. Carnegie may be limited with respect to the actions(s) it can take in response to an anonymous report.</p>
    <p>&nbsp;</p>
    <p><strong>Will I be retaliated against for filing a report?</strong></p>
    <p>Carnegie prohibits any retaliatory action against anyone for submitting a report in good faith or for simply inquiring how to handle potential misconduct or violations of institutional policies.</p>
    <p>&nbsp;</p>
    <p><strong>When can I start using the EthicsPoint Hotline? </strong></p>
    <p>The Hotline/Website are live now and available if you need to report misconduct or a violation.</p>
    <p>To access EthicsPoint, go to MyCarnegie intranet: <a href="https://carnegiescience.ethicspoint.com">https://carnegiescience.ethicspoint.com</a> &nbsp;</p>

