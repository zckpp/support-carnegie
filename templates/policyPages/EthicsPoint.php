<p>Carnegie strives to maintain a positive and pleasant environment for all employees and members of the Carnegie Community.&nbsp; The institution has provided the following options to encourage you to report, in good faith, any violation of Carnegie’s policies and procedures. You may contact Human Resources, the Office of General Counsel, or use the <em>EthicsPoint Hotline/Website.</em></p>
<p><strong>WHAT IS<em> ETHICSPOINT?</em></strong></p>
<p><em>EthicsPoint</em> is an online reporting system that is accessible via phone and website. You may file a report online at <a href="https://carnegiescience.ethicspoint.com">https://carnegiescience.ethicspoint.com</a> &nbsp;or call the 24/7 EthicsPoint Hotline at <u>(844) 680-0876 </u>to securely, confidentially and safely report, activities that may involve misconduct or violations of Carnegie’s institutional policies.</p>

